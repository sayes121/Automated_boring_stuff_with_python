# EXPRESSION
# An expression consist of values and operators
print(2 + 2)
print(3 * 5)
print(4 - 2)
print(6 / 3)

# Order of operation
# Multiplication, Division, Addition and Subtraction
print((2 + 3) * 6)

print(2 + 3 * 6)
print((5-1) * ((7 + 1) / (3 - 1)))
print('\n\n')

# DATA TYPES
# For whole numbers, positive or negative - Integer
print(-2)
print(30)

# For decimal values - Floats
print(3.14)
print(type(3.14))

# For text - Strings
print('Hello World')

# Concatenation in string
print('Sayee' + 'sudha')

# Expressions can also be used for string statements
print('QUT' * 3)
print('Hello' + '!' * 10)
print('\n\n')

# VARIABLES
# It is name given by user which holds a value.
# This value can be a string, int, or float
# when the variable is called,
# it's value will be printed as output
x = 10
print(x)

# The variable data can be changed, by over-riding
x = 'Hello'
print(x + ' World')

# Assignment statement
# expression can be given be variable
# This is an expression
# As it gives a single value as output
x = 2 + 2
print(x)

# This is a statement
# as it will not give any output
x = 6
x = x + 1
